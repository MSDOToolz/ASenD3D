pub fn fabs(f_in : f64) -> f64 {
    f_in.abs()
}

pub fn sqrt(f_in : f64) -> f64 {
    f_in.sqrt()
}

pub fn sin(f_in : f64) -> f64 {
    f_in.sin()
}

pub fn asin(f_in : f64) -> f64 {
    f_in.asin()
}

pub fn cos(f_in : f64) -> f64 {
    f_in.cos()
}

pub fn atan(f_in : f64) -> f64 {
    f_in.atan()
}

pub fn powf(f_in : f64, expnt : f64) -> f64 {
    f_in.powf(expnt)
}